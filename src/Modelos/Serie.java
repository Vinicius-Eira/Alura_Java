package Modelos;

public class Serie extends Titulo{

    private int temporadas;
    private int epPorTemporada;
    private boolean ativa;
    private int minutosPorTemporada;

    public Serie(String nome, int anoDeLancamento) {
        super(nome, anoDeLancamento);
    }

    public int getTemporadas() {
        return temporadas;
    }

    public void setTemporadas(int temporadas) {
        this.temporadas = temporadas;
    }

    public int getEpPorTemporada() {
        return epPorTemporada;
    }

    public void setEpPorTemporada(int epPorTemporada) {
        this.epPorTemporada = epPorTemporada;
    }

    public boolean isAtiva() {
        return ativa;
    }

    public void setAtiva(boolean ativa) {
        this.ativa = ativa;
    }

    public int getMinutosPorTemporada() {
        return minutosPorTemporada;
    }

    public void setMinutosPorTemporada(int minutosPorTemporada) {
        this.minutosPorTemporada = minutosPorTemporada;
    }

    @Override
    public int getDuracaoEmMinutos() {
        return temporadas * epPorTemporada * minutosPorTemporada;
    }

    @Override
    public String toString() {
        return "SÃ©rie: " + this.getNome() + "(" + this.getAnoDeLancamento() + ")";
    }
}
